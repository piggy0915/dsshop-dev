import Network from '../utils/network.js'
export default {
	getList(data,success,fail) {
		Network.setGetMessage('shipping',data,'加载中', function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
	},
	create(data,success,fail) {
		Network.setPostMessage('shipping',data,'处理中', function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
	},
	edit(data,success,fail) {
		Network.setPostMessage('shipping/' + data.id,data,'处理中', function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
	},
	destroy(id,success,fail){
		Network.setPostMessage('shipping/destroy/' + id,{},'处理中', function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
	},
	freight(id,data,success,fail) {
		Network.setPost('shipping/freight/'+id,data, function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
	},
	defaultSet(data,success,fail) {
		Network.setPostMessage('shipping/default/set',data,'处理中', function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
	},
};
